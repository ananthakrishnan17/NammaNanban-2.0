// ─── Supported Languages ──────────────────────────────────────────────────────
enum AppLanguage {
  en('English', 'English', '🇬🇧'),
  ta('Tamil', 'தமிழ்', '🇮🇳'),
  ml('Malayalam', 'മലയാളം', '🇮🇳'),
  te('Telugu', 'తెలుగు', '🇮🇳'),
  kn('Kannada', 'ಕನ್ನಡ', '🇮🇳'),
  hi('Hindi', 'हिन्दी', '🇮🇳');

  final String englishName;
  final String nativeName;
  final String flag;
  const AppLanguage(this.englishName, this.nativeName, this.flag);
}

// ─── Localization Service ─────────────────────────────────────────────────────
class AppLocalizations extends ChangeNotifier {
  static final AppLocalizations instance = AppLocalizations._();
  AppLocalizations._();

  AppLanguage _current = AppLanguage.en;
  AppLanguage get current => _current;

  Future<void> load() async {
    final prefs = await SharedPreferences.getInstance();
    final code = prefs.getString('app_language') ?? 'en';
    _current = AppLanguage.values.firstWhere((l) => l.name == code, orElse: () => AppLanguage.en);
    notifyListeners();
  }

  Future<void> setLanguage(AppLanguage lang) async {
    _current = lang;
    final prefs = await SharedPreferences.getInstance();
    await prefs.setString('app_language', lang.name);
    notifyListeners();
  }

